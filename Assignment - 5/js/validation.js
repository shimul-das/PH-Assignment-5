function validateInputs1(){
  const input1 = document.getElementById("input-1");
  const input2 = document.getElementById("input-2");

  const regex = /^[+]?([0-9]*[.])?[0-9]+$/; // regular expression to match positive float numbers

  if (input1.value === "" || input2.value === "") {
    alert("Inputs cannot be empty!");
    return false;
  } else if (!regex.test(input1.value) || !regex.test(input2.value)) {
    alert("Inputs must be positive or Positive float numbers!");
    return false;
  }
  else{
    return 1;
  }};
